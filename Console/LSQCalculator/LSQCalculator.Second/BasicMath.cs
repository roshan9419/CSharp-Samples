namespace LSQCalculator.Second
{
    class BasicMath
    {
        internal int Add(int numA, int numB)
        {
            return numA + numB;
        }

        internal int Substract(int numA, int numB)
        {
            return numA - numB;
        }

        internal int Multiply(int numA, int numB)
        {
            return numA * numB;
        }

        internal int Divide(int numA, int numB)
        {
            return numA / numB;
        }
    }
}
